import java.lang.*;

public class Variables{
	public static void main(String[] args){
		byte b = 5;
		short s = 300;
		int i = 1000;

		float f = 24.3f;
		double d = 23.456d;

		char c = 'o';

		boolean l = true;

		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(l);
	}
}